    private void jTable_dataBrgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_dataBrgMouseClicked
        // TODO add your handling code here:
        try{
            int row = jTable_dataBrg.getSelectedRow();
            String table_click = (jTable_dataBrg.getModel().getValueAt(row, 0).toString());
            jPanel2.setVisible(true);
            String sql ="SELECT * from master_barang where Kode_brg="+table_click+" ";
            
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
              if (rs.next()){
                    String da1 =rs.getString("Kode_Brg");
                    jTextField5.setText(da1);
                    String da2 =rs.getString("Nama_Brg");
                    jTextField1.setText(da2);
                    String da3 =rs.getString("Harga_jual");
                    jTextField2.setText(da3);
                    String da4 =rs.getString("Harga_beli");
                    jTextField3.setText(da4);
                    String da5 =rs.getString("Satuan");
                    jTextField4.setText(da5);
                    String da6 =rs.getString("Kategori");
                    jTextField6.setText(da6);
                }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTable_dataBrgMouseClicked

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        String sql ="Delete FROM master_barang where Kode_brg=?";
        
        try {
          
           pst = conn.prepareStatement(sql);
           pst.setString(1, jTextField5.getText());
           
           pst.execute();
            JOptionPane.showMessageDialog(null, "Data DiHapus");
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
        jPanel2.setVisible(false);
        DataBarang();
    }//GEN-LAST:event_jButtonDeleteActionPerformed

