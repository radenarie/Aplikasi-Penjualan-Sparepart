    private void DataBarang (){
        try {
            String sql = "SELECT * FROM master_barang order by Nama_Brg asc;";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) jTable_dataBrg.getModel();
                dtm.setRowCount(0);
            String [] data = new String[8];
                int i = 1;
       
        while(rs.next()) {
            data[0] = rs.getString("Kode_Brg");
            data[1] = rs.getString("Nama_Brg");
            data[2] = rs.getString("Harga_jual");
            data[3] = rs.getString("Harga_beli");
            data[4] = rs.getString("Satuan");
            data[5] = rs.getString("Kategori");
            
           
            dtm.addRow(data);
            
            i++;
        }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

